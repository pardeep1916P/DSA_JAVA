/*
  Median of Two Sorted Arrays — Binary Search Partition Method (Optimal Solution)

  Approach:
  - Always apply binary search on the smaller array for efficiency.
  - Partition both arrays such that:
        left portion has half (or half+1) of total elements,
        right portion has the remaining.
  - A correct partition ensures:
        max(left side) ≤ min(right side)
    across both arrays.

  - Once correct partition is found:
        * If total length is odd  → median = max(left elements)
        * If even length          → median = average of
                                    max(left elements) & min(right elements)

  Time  : O(log(min(m, n))) — binary search on smaller array
  Space : O(1)              — uses constant extra memory

  Note:
  This solution is the most optimal and expected in top-tier interviews.
*/

class Solution {

    public double findMedianSortedArrays(int[] nums1, int[] nums2) {

        // Ensure nums1 is the smaller array for binary search
        if (nums1.length > nums2.length) return findMedianSortedArrays(nums2, nums1);

        int m = nums1.length, n = nums2.length;
        int low = 0, high = m;

        while (low <= high) {

            // Partition indices
            int cut1 = (low + high) / 2;
            int cut2 = (m + n + 1) / 2 - cut1;

            // Elements adjacent to the cut on left sides
            int left1  = (cut1 == 0) ? Integer.MIN_VALUE : nums1[cut1 - 1];
            int left2  = (cut2 == 0) ? Integer.MIN_VALUE : nums2[cut2 - 1];

            // Elements adjacent to the cut on right sides
            int right1 = (cut1 == m) ? Integer.MAX_VALUE : nums1[cut1];
            int right2 = (cut2 == n) ? Integer.MAX_VALUE : nums2[cut2];

            // ✔ Found correct partition
            if (left1 <= right2 && left2 <= right1) {

                // Odd total length → return middle element
                if ((m + n) % 2 == 1) return Math.max(left1, left2);

                // Even → median is avg of two center values
                return (Math.max(left1, left2) + Math.min(right1, right2)) / 2.0;
            }

            // Move search towards left side
            else if (left1 > right2) high = cut1 - 1;

            // Move search towards right side
            else low = cut1 + 1;
        }
        return 0.0; // Should never reach here
    }
}